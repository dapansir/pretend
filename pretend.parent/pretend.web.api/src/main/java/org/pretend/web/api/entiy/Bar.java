package org.pretend.web.api.entiy;

public class Bar {
	
	private String name;
	
	private String href;
	
	private int order;

	private String tipName;
	
	private String tipType;
	
	private String tipBackGroundUrl;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getTipName() {
		return tipName;
	}

	public void setTipName(String tipName) {
		this.tipName = tipName;
	}

	public String getTipType() {
		return tipType;
	}

	public void setTipType(String tipType) {
		this.tipType = tipType;
	}

	public String getTipBackGroundUrl() {
		return tipBackGroundUrl;
	}

	public void setTipBackGroundUrl(String tipBackGroundUrl) {
		this.tipBackGroundUrl = tipBackGroundUrl;
	}
	
	
}
